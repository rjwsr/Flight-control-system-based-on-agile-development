q = 1;
n = 0.000001; %透射率
k = 1;
for u = 0.1:0.05:0.51 %光强
Em = 0.01275;
a = 0.001;
l = 10000;
n = (10)^(-(a*(l/2))/10);
Pdc = 0.00000001;
n1 = 0.03*n;
Qu = 1-((1-Pdc)^2)*(exp(1))^(-(u*n1));
w = 0.0001;
v = 0.01;
eotp = 0.03;
Qw = 1-((1-Pdc)^2)*(exp(1))^(-w*n1);
Qv = 1-((1-Pdc)^2)*(exp(1))^(-v*n1);
y0 = (v*Qw*((exp(1))^w)-w*Qv*((exp(1))^v))/(v-w);
y1 = ((u^2)*Qv*((exp(1))^v)-(u^2)*Qw*((exp(1))^w)-((v^2)-(w^2))*((Qu*(exp(1))^u)-y0))/(u*(u*v-u*w-(v^2)+(w^2)));
Eu = 1/2+(1/(2*Qu))*(1-Pdc)*((exp(1))^(-(u*n1*(1-(eotp+Em))))-(exp(1))^(-(u*n1*(eotp+Em))));
Ev = 1/2+(1/(2*Qv))*(1-Pdc)*((exp(1))^(-(v*n1*(1-(eotp+Em))))-(exp(1))^(-(v*n1*(eotp+Em))));
Ew = 1/2+(1/(2*Qw))*(1-Pdc)*((exp(1))^(-(w*n1*(1-(eotp+Em))))-(exp(1))^(-(w*n1*(eotp+Em))));
e1 = (Ev*Qv*((exp(1))^v)-Ew*Qw*((exp(1))^w))/((v-w)*y1);
P1 = (exp(1)^(-u))*u;
Q1 = (u*(exp(1))^(-u))*y1;
D1 = Q1/Qu;
R(k) = q*Qu*((-1)*H(Eu)+D1*(1-H(e1)));
k = k+1;
end
plot(R);